function confirmDelete(url) {
  // Ask the user to confirm the deletion
  const userConfirmed = confirm('Are you sure you want to delete this record?');

  if (userConfirmed) {
    // If confirmed, navigate to the delete URL
    window.location.href = url;
  }
}
